USE vsms_customer;

-- 1. customers - Customer master table
CREATE TABLE IF NOT EXISTS customers (
    id BINARY(16) NOT NULL PRIMARY KEY,
    customer_name VARCHAR(255) NOT NULL,
    customer_type VARCHAR(50) NOT NULL,
    oem_code VARCHAR(100),
    address VARCHAR(500),
    address2 VARCHAR(500),
    address3 VARCHAR(500),
    city VARCHAR(100),
    pin_code VARCHAR(10),
    phone VARCHAR(20),
    fax VARCHAR(20),
    email VARCHAR(255),
    website VARCHAR(255),
    contact_person_name VARCHAR(255),
    contact_person_phone VARCHAR(20),
    industry VARCHAR(100),
    branch_area VARCHAR(100),
    gst_number VARCHAR(15),
    pan_number VARCHAR(10),
    state_code VARCHAR(10),
    location_id BIGINT,
    state_id BIGINT,
    country_id BIGINT,
    customer_code INT,
    email_customer_id INT,
    remarks TEXT,
    approval_status ENUM('PENDING', 'APPROVED', 'REJECTED') DEFAULT 'PENDING',
    is_active BOOLEAN DEFAULT TRUE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    created_by VARCHAR(100),
    updated_by VARCHAR(100),
    version BIGINT DEFAULT 0
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- Create indexes for better performance
CREATE INDEX idx_customer_name ON customers (customer_name);
CREATE INDEX idx_customer_type ON customers (customer_type);
CREATE INDEX idx_email ON customers (email);
CREATE INDEX idx_gst_number ON customers (gst_number);
CREATE INDEX idx_pan_number ON customers (pan_number);
CREATE INDEX idx_customer_code ON customers (customer_code);
CREATE INDEX idx_approval_status ON customers (approval_status);
CREATE INDEX idx_is_active ON customers (is_active);
CREATE INDEX idx_location_id ON customers (location_id);
CREATE INDEX idx_state_id ON customers (state_id);
CREATE INDEX idx_country_id ON customers (country_id);
CREATE INDEX idx_created_by ON customers (created_by);
CREATE INDEX idx_created_at ON customers (created_at);

-- Create unique constraints
ALTER TABLE customers ADD CONSTRAINT uk_customer_name_active UNIQUE (customer_name, is_active);
ALTER TABLE customers ADD CONSTRAINT uk_gst_number_active UNIQUE (gst_number, is_active);
ALTER TABLE customers ADD CONSTRAINT uk_pan_number_active UNIQUE (pan_number, is_active);
ALTER TABLE customers ADD CONSTRAINT uk_customer_code_active UNIQUE (customer_code, is_active);

